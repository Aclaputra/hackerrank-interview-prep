import java.io.*;
import java.util.*;

class Solution {
  static Scanner sc = new Scanner(System.in);
  static final PrintStream out = System.out;

  static void solve() {

    out.println("hi");
  }
  public static void main(String $[]) {
    int T = sc.nextInt();
    for (int t=1; t<=T; ++t) {
      out.println("Case #"+t+": ");
      solve();
    }
  }
}
